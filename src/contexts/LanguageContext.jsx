// Provider du contexte
export const LanguageProvider = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState('fr');
  const [translations, setTranslations] = useState(defaultTranslations);
  const [isLoading, setIsLoading] = useState(false);

  // Charger la langue depuis le localStorage au d√©marrage
  useEffect(() => {
    const savedLanguage = localStorage.getItem('preferredLanguage');
    if (savedLanguage && availableLanguages.find(lang => lang.code === savedLanguage)) {
      setCurrentLanguage(savedLanguage);
      loadTranslations(savedLanguage);
    } else {
      // D√©finir 'fr' comme langue par d√©faut si rien n'est trouv√©
      setCurrentLanguage('fr');
      localStorage.setItem('preferredLanguage', 'fr');
      loadTranslations('fr');
    }
  }, []);

  // Fonction pour charger les traductions
  const loadTranslations = async (languageCode) => {
    setIsLoading(true);
    try {
      console.log(`üåê Chargement des traductions pour: ${languageCode}`);
      
      // Charger le fichier de traduction
      const translationModule = await import(`../locales/${languageCode}.json`);
      const loadedTranslations = translationModule.default || translationModule;
      
      // Fusionner avec les traductions par d√©faut
      const mergedTranslations = {
        ...defaultTranslations,
        ...loadedTranslations
      };
      
      setTranslations(mergedTranslations);
      
      // Mettre √† jour l'attribut lang du document
      document.documentElement.lang = languageCode;
      
      // D√©clencher un √©v√©nement personnalis√© pour notifier les composants
      window.dispatchEvent(new CustomEvent('languageChanged', { 
        detail: { language: languageCode, translations: mergedTranslations } 
      }));
      
      console.log(`‚úÖ Traductions charg√©es pour ${languageCode}:`, Object.keys(mergedTranslations).length, 'cl√©s');
      
    } catch (error) {
      console.warn(`‚ö†Ô∏è Impossible de charger les traductions pour ${languageCode}:`, error);
      // Utiliser les traductions par d√©faut en cas d'erreur
      setTranslations(defaultTranslations);
    } finally {
      setIsLoading(false);
    }
  };

  // Fonction pour changer de langue
  const setLanguage = async (languageCode) => {
    console.log(`üîÑ Changement de langue vers: ${languageCode}`);
    
    if (availableLanguages.find(lang => lang.code === languageCode)) {
      setCurrentLanguage(languageCode);
      localStorage.setItem('preferredLanguage', languageCode);
      await loadTranslations(languageCode);
    } else {
      console.error(`‚ùå Langue non support√©e: ${languageCode}`);
    }
  };

  // Fonction pour obtenir une traduction avec fallback
  const getTranslation = (key, fallback = key) => {
    return translations[key] || fallback;
  };

  // Fonction pour obtenir une traduction avec interpolation
  const getTranslationWithVars = (key, variables = {}, fallback = key) => {
    let translation = translations[key] || fallback;
    
    // Remplacer les variables dans la traduction
    Object.keys(variables).forEach(varKey => {
      const placeholder = `{{${varKey}}}`;
      translation = translation.replace(new RegExp(placeholder, 'g'), variables[varKey]);
    });
    
    return translation;
  };

  const contextValue = {
    currentLanguage,
    setLanguage,
    availableLanguages,
    translations,
    getTranslation,
    getTranslationWithVars,
    isLoading
  };

  return (
    <LanguageContext.Provider value={contextValue}>
      {children}
    </LanguageContext.Provider>
  );
};

// Hook pour utiliser le contexte
export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

export default LanguageContext;
